import jsPDF from 'jspdf';
import 'jspdf-autotable';
import { fetchImageAsBase64 } from '../utils/image-loader.js';

export async function generatePDF(products, coverInfo) {
  const doc = new jsPDF({ orientation: 'portrait', unit: 'mm', format: 'a4' });
  const pageWidth = doc.internal.pageSize.getWidth();
  const pageHeight = doc.internal.pageSize.getHeight();
  const margin = 20;

  // Colors
  const wine = [30, 58, 138];       // Navy Blue #1E3A8A
  const wineDark = [30, 64, 175];   // Darker Navy #1E40AF
  const beige = [255, 255, 255];    // White #FFFFFF
  const cream = [248, 250, 252];    // Slate 50 #F8FAFC
  const textDark = [15, 23, 42];    // Slate 900 #0F172A
  const textBody = [51, 65, 85];    // Slate 700 #334155
  const textMuted = [148, 163, 184]; // Slate 400 #94A3B8
  const gold = [59, 130, 246];      // Blue 500 #3B82F6

  // ---- Cover Page ----
  // Wine header bar
  doc.setFillColor(...wine);
  doc.rect(0, 0, pageWidth, 80, 'F');

  // Title
  doc.setTextColor(255, 255, 255);
  doc.setFontSize(28);
  doc.setFont('helvetica', 'bold');
  doc.text(coverInfo.title || 'Product Catalogue', pageWidth / 2, 40, { align: 'center' });

  // Subtitle
  if (coverInfo.subtitle) {
    doc.setFontSize(14);
    doc.setFont('helvetica', 'normal');
    doc.text(coverInfo.subtitle, pageWidth / 2, 55, { align: 'center' });
  }

  // Decorative gold line
  doc.setDrawColor(...gold);
  doc.setLineWidth(0.8);
  doc.line(margin + 30, 90, pageWidth - margin - 30, 90);

  // Meta info
  doc.setTextColor(...textBody);
  doc.setFontSize(11);
  doc.setFont('helvetica', 'normal');

  let metaY = 110;
  if (coverInfo.companyName) {
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(16);
    doc.text(coverInfo.companyName, pageWidth / 2, metaY, { align: 'center' });
    metaY += 12;
  }

  doc.setFont('helvetica', 'normal');
  doc.setFontSize(11);
  doc.setTextColor(...textMuted);
  doc.text(`Date: ${coverInfo.date || new Date().toLocaleDateString('en-US')}`, pageWidth / 2, metaY, { align: 'center' });
  metaY += 8;
  doc.text(`${products.length} Products`, pageWidth / 2, metaY, { align: 'center' });

  // Footer on cover
  doc.setFontSize(8);
  doc.setTextColor(...textMuted);
  doc.text('Generated by Oliver Mc Inroy Catalogue', pageWidth / 2, pageHeight - 15, { align: 'center' });

  // ---- Product Pages ----
  for (let idx = 0; idx < products.length; idx++) {
    const product = products[idx];
    doc.addPage();

    // Page header bar
    doc.setFillColor(...wine);
    doc.rect(0, 0, pageWidth, 12, 'F');

    // Product number
    doc.setFontSize(9);
    doc.setTextColor(255, 255, 255);
    doc.text(`Product ${idx + 1} of ${products.length}`, margin, 8);
    doc.text(coverInfo.title || 'Product Catalogue', pageWidth - margin, 8, { align: 'right' });

    let y = 25;

    // Product name
    doc.setTextColor(...textDark);
    doc.setFontSize(20);
    doc.setFont('helvetica', 'bold');
    doc.text(product.name, margin, y);
    y += 10;

    // Gold separator
    doc.setDrawColor(...gold);
    doc.setLineWidth(0.5);
    doc.line(margin, y, margin + 40, y);
    y += 12;

    if (product.imageUrl) {
      try {
        const base64Img = await fetchImageAsBase64(product.imageUrl);
        if (base64Img) {
          const imgProps = doc.getImageProperties(base64Img);
          const imgWidth = 60; // mm
          const imgHeight = (imgProps.height * imgWidth) / imgProps.width;
          
          doc.addImage(base64Img, 'JPEG', margin, y, imgWidth, imgHeight);
          y += imgHeight + 10;
        } else {
          doc.setFontSize(10);
          doc.setTextColor(...textMuted);
          doc.text("[Image Unavailable]", margin, y + 5);
          y += 15;
        }
      } catch (e) {
        console.warn('PDF Image error:', e);
      }
    }

    // Product details table
    const tableData = [];

    if (product.price !== undefined && product.price !== null) {
      tableData.push(['Price', `$${product.price.toLocaleString('en-US')}`]);
    }
    if (product.size) {
      tableData.push(['Size', product.size]);
    }
    if (product.materials && product.materials.length > 0) {
      tableData.push(['Materials', product.materials.join(', ')]);
    } else if (product.material) {
      tableData.push(['Materials', product.material]);
    }
    if (product.categories && product.categories.length > 0) {
      tableData.push(['Product Categories', product.categories.join(', ')]);
    }
    if (product.buyerCategories && product.buyerCategories.length > 0) {
      tableData.push(['Buyer Categories', product.buyerCategories.join(', ')]);
    }

    doc.autoTable({
      startY: y,
      margin: { left: margin, right: margin },
      head: [['Specification', 'Details']],
      body: tableData,
      theme: 'grid',
      headStyles: {
        fillColor: wine,
        textColor: [255, 255, 255],
        fontStyle: 'bold',
        fontSize: 10,
      },
      bodyStyles: {
        fontSize: 10,
        textColor: textBody,
        cellPadding: 6,
      },
      alternateRowStyles: {
        fillColor: cream,
      },
      styles: {
        lineColor: [226, 216, 204],
        lineWidth: 0.3,
      },
      columnStyles: {
        0: { fontStyle: 'bold', cellWidth: 45, textColor: textDark },
      },
    });

    // Page number footer
    doc.setFontSize(8);
    doc.setTextColor(...textMuted);
    doc.text(`Page ${idx + 2}`, pageWidth / 2, pageHeight - 10, { align: 'center' });
  }

  // Save
  const fileName = (coverInfo.title || 'catalogue').replace(/[^a-zA-Z0-9]/g, '_').toLowerCase();
  doc.save(`${fileName}.pdf`);
}
