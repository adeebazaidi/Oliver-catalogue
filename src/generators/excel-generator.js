import ExcelJS from 'exceljs';
import { saveAs } from 'file-saver';
import { fetchImageAsBase64 } from '../utils/image-loader.js';

export async function generateExcel(products, coverInfo) {
  const workbook = new ExcelJS.Workbook();
  workbook.creator = coverInfo.companyName || 'Oliver Mc Inroy Catalogue';
  workbook.created = new Date();

  // Brand colors
  const wine = 'FF1E3A8A';
  const wineDark = 'FF1E40AF';
  const beige = 'FFFFFFFF';
  const cream = 'FFF8FAFC';
  const gold = 'FF3B82F6';
  const white = 'FFFFFFFF';
  const textDark = 'FF0F172A';
  const textBody = 'FF334155';
  const borderColor = 'FFE2E8F0';

  const fontBody = 'Segoe UI';
  const fontHeading = 'Georgia';

  // ---- Cover Sheet ----
  const coverSheet = workbook.addWorksheet('Cover', {
    properties: { tabColor: { argb: wine } },
  });

  coverSheet.mergeCells('A1:F1');
  coverSheet.mergeCells('A3:F3');
  coverSheet.mergeCells('A4:F4');
  coverSheet.mergeCells('A5:F5');
  coverSheet.mergeCells('A6:F6');
  coverSheet.mergeCells('A8:F8');

  // Title row
  const titleRow = coverSheet.getRow(1);
  titleRow.height = 50;
  const titleCell = coverSheet.getCell('A1');
  titleCell.value = coverInfo.title || 'Product Catalogue';
  titleCell.font = { name: fontHeading, size: 24, bold: true, color: { argb: white } };
  titleCell.alignment = { horizontal: 'center', vertical: 'middle' };
  titleCell.fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: wine } };

  // Empty row 2
  coverSheet.getRow(2).height = 10;

  // Subtitle
  if (coverInfo.subtitle) {
    const subtitleCell = coverSheet.getCell('A3');
    subtitleCell.value = coverInfo.subtitle;
    subtitleCell.font = { name: fontBody, size: 14, color: { argb: textBody } };
    subtitleCell.alignment = { horizontal: 'center', vertical: 'middle' };
  }

  // Company
  if (coverInfo.companyName) {
    const companyCell = coverSheet.getCell('A4');
    companyCell.value = coverInfo.companyName;
    companyCell.font = { name: fontBody, size: 12, bold: true, color: { argb: textDark } };
    companyCell.alignment = { horizontal: 'center' };
  }

  // Date
  const dateCell = coverSheet.getCell('A5');
  dateCell.value = `Date: ${coverInfo.date || new Date().toLocaleDateString('en-IN')}`;
  dateCell.font = { name: fontBody, size: 11, color: { argb: textBody } };
  dateCell.alignment = { horizontal: 'center' };

  // Product count
  const countCell = coverSheet.getCell('A6');
  countCell.value = `${products.length} Products`;
  countCell.font = { name: fontBody, size: 11, color: { argb: 'FF8B7B74' } };
  countCell.alignment = { horizontal: 'center' };

  // Generated by
  const genCell = coverSheet.getCell('A8');
  genCell.value = 'Generated by Oliver Mc Inroy Catalogue';
  genCell.font = { name: fontBody, size: 9, italic: true, color: { argb: 'FFB5A89F' } };
  genCell.alignment = { horizontal: 'center' };

  // Set column widths on cover
  coverSheet.columns = [
    { width: 15 }, { width: 15 }, { width: 15 }, { width: 15 }, { width: 15 }, { width: 15 },
  ];

  // ---- Products Sheet ----
  const prodSheet = workbook.addWorksheet('Products', {
    properties: { tabColor: { argb: gold } },
  });

  // Column definitions
  prodSheet.columns = [
    { header: '#', key: 'index', width: 6 },
    { header: 'Image', key: 'image', width: 15 },
    { header: 'Product Name', key: 'name', width: 32 },
    { header: 'Size', key: 'size', width: 18 },
    { header: 'Price (₹)', key: 'price', width: 16 },
    { header: 'Material', key: 'material', width: 20 },
    { header: 'Categories', key: 'categories', width: 28 },
  ];

  // Style header row
  const headerRow = prodSheet.getRow(1);
  headerRow.height = 32;
  headerRow.eachCell((cell) => {
    cell.font = { name: fontBody, size: 11, bold: true, color: { argb: white } };
    cell.fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: wine } };
    cell.alignment = { horizontal: 'center', vertical: 'middle' };
    cell.border = {
      bottom: { style: 'thin', color: { argb: borderColor } },
    };
  });

  // Add product rows
  for (let idx = 0; idx < products.length; idx++) {
    const product = products[idx];
    const row = prodSheet.addRow({
      index: idx + 1,
      image: '',
      name: product.name,
      size: product.size || '',
      price: product.price,
      material: product.material || '',
      categories: product.categories.join(', '),
    });

    row.height = 65; // Make row taller for image

    // Alternating row colors
    const fillColor = idx % 2 === 0 ? white : cream;
    row.eachCell((cell) => {
      cell.font = { name: fontBody, size: 10, color: { argb: textBody } };
      cell.fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: fillColor } };
      cell.alignment = { vertical: 'middle', wrapText: true };
      cell.border = {
        bottom: { style: 'hair', color: { argb: borderColor } },
      };
    });

    // Bold product name
    row.getCell('name').font = { name: fontBody, size: 10, bold: true, color: { argb: textDark } };

    // Format price cell as number
    const priceCell = row.getCell('price');
    priceCell.numFmt = '#,##0.00';
    priceCell.alignment = { horizontal: 'right', vertical: 'middle' };

    // Image integration
    if (product.imageUrl) {
      try {
        const base64Img = await fetchImageAsBase64(product.imageUrl);
        if (base64Img) {
          const imageId = workbook.addImage({
            base64: base64Img,
            extension: 'png',
          });
          prodSheet.addImage(imageId, {
            tl: { col: 1.1, row: idx + 1.1 },
            ext: { width: 70, height: 70 },
          });
        }
      } catch (e) {
        console.warn('Excel Image error:', e);
      }
    }
  }

  // Summary row
  const summaryRowNum = products.length + 2;
  prodSheet.mergeCells(`A${summaryRowNum}:D${summaryRowNum}`);
  const summaryLabel = prodSheet.getCell(`A${summaryRowNum}`);
  summaryLabel.value = `Total: ${products.length} Products`;
  summaryLabel.font = { name: fontBody, size: 10, bold: true, color: { argb: textDark } };

  const summaryPrice = prodSheet.getCell(`E${summaryRowNum}`);
  summaryPrice.value = { formula: `SUM(E2:E${products.length + 1})` };
  summaryPrice.numFmt = '#,##0.00';
  summaryPrice.font = { name: fontBody, size: 10, bold: true, color: { argb: wine.slice(2) } };
  summaryPrice.alignment = { horizontal: 'right' };

  // Auto-filter
  prodSheet.autoFilter = { from: 'A1', to: `G${products.length + 1}` };

  // Freeze header row
  prodSheet.views = [{ state: 'frozen', ySplit: 1 }];

  // Save
  const buffer = await workbook.xlsx.writeBuffer();
  const fileName = (coverInfo.title || 'catalogue').replace(/[^a-zA-Z0-9]/g, '_').toLowerCase();
  saveAs(new Blob([buffer], { type: 'application/vnd.openxmlformats-officedocument.spreadsheetml.sheet' }), `${fileName}.xlsx`);
}
